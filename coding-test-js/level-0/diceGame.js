// 코드 리뷰 때 인상 깊었던 코드

function solution(a, b, c) {
  // set은 중복을 허용하지 않는 집합임
  // [a, b, c]를 Set에 넣으면 같은 값들은 하나로 합쳐짐
  // size를 통해 값의 갯수를 셈
  // 3이면 모두 다름, 2이면 두개만 같음, 1이면 모두 같음
  const dsttCnt = new Set([a, b, c]).size;

  switch (dsttCnt) {
    case 3: // 갯수에 따라, 3이면 모두 다름
      return a + b + c;
    case 2: // 2개만 같음
      return (a + b + c) * (a ** 2 + b ** 2 + c ** 2);
    case 1: // 3개 모두 같은 숫자임
      return (
        (a + b + c) * (a ** 2 + b ** 2 + c ** 2) * (a ** 3 + b ** 3 + c ** 3)
      );
  }
}

